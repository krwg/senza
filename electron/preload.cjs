const { contextBridge, ipcRenderer } = require('electron');

contextBridge.exposeInMainWorld('senza', {
  getState: () => ipcRenderer.invoke('senza:get-state'),
  saveState: (state) => ipcRenderer.invoke('senza:save-state', state),
  getLibraryRoot: () => ipcRenderer.invoke('senza:get-library-root'),
  pickFiles: () => ipcRenderer.invoke('senza:pick-files'),
  pickFolder: () => ipcRenderer.invoke('senza:pick-folder'),
  importPaths: (paths) => ipcRenderer.invoke('senza:import-paths', paths),
  openPath: (filePath) => ipcRenderer.invoke('senza:open-path', filePath),
  fileUrl: (filePath) => ipcRenderer.invoke('senza:file-url', filePath),
  coverUrl: (trackId) => ipcRenderer.invoke('senza:cover-url', trackId),
  artistAvatarUrl: (slug) => ipcRenderer.invoke('senza:artist-avatar-url', slug),
  artistAvatarSave: (payload) => ipcRenderer.invoke('senza:artist-avatar-save', payload),
  confirmRemoveTrack: (locale) => ipcRenderer.invoke('senza:confirm-remove-track', { locale }),
  confirmBulkRemove: (count, locale) => ipcRenderer.invoke('senza:confirm-bulk-remove', { count, locale }),
  removeTrack: (payload) => ipcRenderer.invoke('senza:remove-track', payload),
  windowMinimize: () => ipcRenderer.invoke('senza:window-minimize'),
  windowToggleMaximize: () => ipcRenderer.invoke('senza:window-toggle-maximize'),
  windowClose: () => ipcRenderer.invoke('senza:window-close'),
  readTags: (filePath) => ipcRenderer.invoke('senza:read-tags', filePath),
  writeTags: (payload) => ipcRenderer.invoke('senza:write-tags', payload),
  pickCover: () => ipcRenderer.invoke('senza:pick-cover'),
  readFileBinary: (filePath) => ipcRenderer.invoke('senza:read-file-binary', filePath),
  libraryTree: () => ipcRenderer.invoke('senza:library-tree'),
  profileGet: () => ipcRenderer.invoke('senza:profile-get'),
  profileSave: (profile) => ipcRenderer.invoke('senza:profile-save', profile),
  profileAvatarUrl: () => ipcRenderer.invoke('senza:profile-avatar-url'),
  playlistsList: () => ipcRenderer.invoke('senza:playlists-list'),
  playlistCreate: (name) => ipcRenderer.invoke('senza:playlist-create', name),
  playlistSave: (playlist) => ipcRenderer.invoke('senza:playlist-save', playlist),
  playlistDelete: (slug) => ipcRenderer.invoke('senza:playlist-delete', slug),
  glyphLearnLog: (payload) => ipcRenderer.invoke('senza:glyph-learn-log', payload),
  glyphLearnStats: () => ipcRenderer.invoke('senza:glyph-learn-stats'),
  glyphLearnExport: (opts) => ipcRenderer.invoke('senza:glyph-learn-export', opts),
  glyphOpenExports: () => ipcRenderer.invoke('senza:glyph-open-exports'),
  glyphMiStatus: (opts) => ipcRenderer.invoke('senza:glyph-mi-status', opts || {}),
  glyphAnalyze: (input) => ipcRenderer.invoke('senza:glyph-analyze', input),
  glyphVaultScan: (tracks, maxFixPreview) =>
    ipcRenderer.invoke('senza:glyph-vault-scan', { tracks, maxFixPreview }),
  glyphReloadKnowledge: () => ipcRenderer.invoke('senza:glyph-reload-knowledge'),
  glyphMusicBrainzLookup: (query) => ipcRenderer.invoke('senza:glyph-musicbrainz-lookup', query),
  glyphAcoustidLookup: (payload) => ipcRenderer.invoke('senza:glyph-acoustid-lookup', payload),
  glyphFingerprint: (payload) => ipcRenderer.invoke('senza:glyph-fingerprint', payload),
  glyphOnlineStatus: () => ipcRenderer.invoke('senza:glyph-online-status'),
  glyphDownloadTools: () => ipcRenderer.invoke('senza:glyph-download-tools'),
  glyphImportExport: () => ipcRenderer.invoke('senza:glyph-import-export'),
  glyphLibraryFeatures: () => ipcRenderer.invoke('senza:glyph-library-features'),
  glyphDbUpsert: (track) => ipcRenderer.invoke('senza:glyph-db-upsert', { track }),
  glyphDbSync: (tracks) => ipcRenderer.invoke('senza:glyph-db-sync', { tracks }),
  glyphLearnedPack: () => ipcRenderer.invoke('senza:glyph-learned-pack'),
  glyphRebuildLearned: () => ipcRenderer.invoke('senza:glyph-rebuild-learned'),
  glyphLog: (payload) => ipcRenderer.invoke('senza:glyph-log', payload),
  glyphLogStats: () => ipcRenderer.invoke('senza:glyph-log-stats'),
  glyphLogExportDataset: (opts) => ipcRenderer.invoke('senza:glyph-log-export-dataset', opts || {}),
  glyphAnalytics: () => ipcRenderer.invoke('senza:glyph-analytics'),
});
