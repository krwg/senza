export function detectLocale() {
  const saved = localStorage.getItem('senza-lang');
  if (saved === 'en' || saved === 'ru') return saved;
  return navigator.language?.toLowerCase().startsWith('ru') ? 'ru' : 'en';
}

export function t(key, locale) {
  return strings[locale]?.[key] ?? strings.en[key] ?? key;
}

export function tf(key, locale, vars = {}) {
  let s = t(key, locale);
  for (const [k, v] of Object.entries(vars)) {
    s = s.replace(`{${k}}`, String(v));
  }
  return s;
}

export function applyI18n(locale) {
  document.documentElement.lang = locale;
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key, locale);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = t(key, locale);
  });
  document.querySelectorAll('[data-i18n-title]').forEach((el) => {
    const key = el.getAttribute('data-i18n-title');
    el.title = t(key, locale);
    if (el.hasAttribute('aria-label')) el.setAttribute('aria-label', t(key, locale));
  });
}
